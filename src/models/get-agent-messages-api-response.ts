/**
 * Message types supported by the Letta API.
 */
export enum MessageType {
  System = "system_message",
  User = "user_message",
  Reasoning = "reasoning_message",
  ToolCall = "tool_call_message",
  ToolReturn = "tool_return_message",
  Assistant = "assistant_message",
}

/**
 * Base interface for all message types.
 * Includes common properties like `id` and `date`.
 */
interface MessageBase {
  id?: string; // The unique identifier for the message (optional).
  date?: string; // The timestamp when the message was created (ISO format, optional).
}

/**
 * Represents a system-generated message.
 */
interface SystemMessage extends MessageBase {
  message_type: MessageType.System; // Indicates this is a system message.
  message: string; // The content of the system message.
}

/**
 * Represents a message sent by the user.
 */
interface UserMessage extends MessageBase {
  message_type: MessageType.User; // Indicates this is a user message.
  message: string; // The content of the user message.
}

/**
 * Represents an agent's internal reasoning.
 */
interface ReasoningMessage extends MessageBase {
  message_type: MessageType.Reasoning; // Indicates this is a reasoning message.
  reasoning: string; // The internal reasoning of the agent.
}

/**
 * Represents the function details within a tool call.
 */
interface ToolCallFunction {
  name: string; // The name of the function to call.
  arguments: string; // The arguments to pass to the function (JSON dump).
}

